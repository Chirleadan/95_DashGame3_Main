/**
 * Converts public/audio/buttons/*.wav to compact .ogg (Opus).
 * Requires: npm install (ffmpeg-static is a devDependency).
 *
 * Usage: node tools/convert-button-sfx-to-opus.mjs
 */
import { spawnSync } from 'node:child_process';
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import ffmpegPath from 'ffmpeg-static';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const projectRoot = path.resolve(__dirname, '..');
const buttonsDir = path.join(projectRoot, 'public', 'audio', 'buttons');
const catalogPath = path.join(projectRoot, 'src', 'game', 'ButtonSfxCatalog.ts');

/** @type {readonly { wav: string; ogg: string; key: string }[]} */
const MAP = [
  { wav: 'target.wav', ogg: 'target.ogg', key: 'target1' },
  { wav: 'target2.wav', ogg: 'target2.ogg', key: 'target2' },
  { wav: 'press1.wav', ogg: 'press1.ogg', key: 'press1' },
  { wav: 'press2.wav', ogg: 'press2.ogg', key: 'press2' },
  { wav: 'play.wav', ogg: 'play.ogg', key: 'play' },
];

if (!ffmpegPath || !fs.existsSync(ffmpegPath)) {
  console.error('ffmpeg-static binary not found. Run: npm install');
  process.exit(1);
}

function runFfmpeg(args) {
  const result = spawnSync(ffmpegPath, args, { stdio: 'inherit' });
  if (result.status !== 0) {
    process.exit(result.status ?? 1);
  }
}

for (const { wav, ogg } of MAP) {
  const input = path.join(buttonsDir, wav);
  const output = path.join(buttonsDir, ogg);
  if (!fs.existsSync(input)) {
    console.error(`Missing input: ${input}`);
    process.exit(1);
  }
  console.log(`\n→ ${wav} → ${ogg}`);
  runFfmpeg([
    '-y',
    '-i',
    input,
    '-vn',
    '-c:a',
    'libopus',
    '-b:a',
    '40k',
    '-vbr',
    'on',
    '-compression_level',
    '10',
    '-application',
    'audio',
    output,
  ]);
  const inKb = (fs.statSync(input).size / 1024).toFixed(1);
  const outKb = (fs.statSync(output).size / 1024).toFixed(1);
  const pct = ((1 - fs.statSync(output).size / fs.statSync(input).size) * 100).toFixed(0);
  console.log(`  ${inKb} KB → ${outKb} KB (−${pct}%)`);
}

const catalog = `/** @generated by tools/convert-button-sfx-to-opus.mjs — do not edit by hand */
const BUTTON_SFX_DIR = '/audio/buttons';

export const BUTTON_SFX_TARGET_URLS = [
  \`\${BUTTON_SFX_DIR}/target.ogg\`,
  \`\${BUTTON_SFX_DIR}/target2.ogg\`,
] as const;

export const BUTTON_SFX_PRESS_URLS = [
  \`\${BUTTON_SFX_DIR}/press1.ogg\`,
  \`\${BUTTON_SFX_DIR}/press2.ogg\`,
] as const;

export const BUTTON_SFX_PLAY_URL = \`\${BUTTON_SFX_DIR}/play.ogg\`;

export const BUTTON_SFX_ALL_URLS: readonly string[] = [
  ...BUTTON_SFX_TARGET_URLS,
  ...BUTTON_SFX_PRESS_URLS,
  BUTTON_SFX_PLAY_URL,
];
`;

fs.writeFileSync(catalogPath, catalog, 'utf8');
console.log(`\nWrote ${path.relative(projectRoot, catalogPath)}`);
