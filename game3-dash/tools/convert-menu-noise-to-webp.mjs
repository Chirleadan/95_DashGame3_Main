/**
 * Converts public/assets/menu noise/*.png to .webp and regenerates MenuNoiseCatalog.ts.
 * Requires: npm install (ffmpeg-static is a devDependency).
 *
 * Usage: node tools/convert-menu-noise-to-webp.mjs
 */
import { spawnSync } from 'node:child_process';
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import ffmpegPath from 'ffmpeg-static';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const projectRoot = path.resolve(__dirname, '..');
const noiseDir = path.join(projectRoot, 'public', 'assets', 'menu noise');
const catalogPath = path.join(projectRoot, 'src', 'game', 'MenuNoiseCatalog.ts');

if (!ffmpegPath || !fs.existsSync(ffmpegPath)) {
  console.error('ffmpeg-static binary not found. Run: npm install');
  process.exit(1);
}

if (!fs.existsSync(noiseDir)) {
  console.error(`Missing folder: ${noiseDir}`);
  process.exit(1);
}

function runFfmpeg(args) {
  const result = spawnSync(ffmpegPath, args, { stdio: 'inherit' });
  if (result.status !== 0) {
    process.exit(result.status ?? 1);
  }
}

const pngFiles = fs
  .readdirSync(noiseDir)
  .filter((name) => name.toLowerCase().endsWith('.png'))
  .sort((a, b) => a.localeCompare(b, undefined, { numeric: true }));

if (pngFiles.length === 0) {
  console.error('No PNG files found in menu noise folder.');
  process.exit(1);
}

const webpFiles = [];

for (const png of pngFiles) {
  const input = path.join(noiseDir, png);
  const webp = png.replace(/\.png$/i, '.webp');
  const output = path.join(noiseDir, webp);
  console.log(`\n→ ${png} → ${webp}`);
  runFfmpeg([
    '-y',
    '-i',
    input,
    '-c:v',
    'libwebp',
    '-lossless',
    '0',
    '-quality',
    '82',
    '-preset',
    'default',
    output,
  ]);
  const inKb = (fs.statSync(input).size / 1024).toFixed(1);
  const outKb = (fs.statSync(output).size / 1024).toFixed(1);
  const pct = ((1 - fs.statSync(output).size / fs.statSync(input).size) * 100).toFixed(0);
  console.log(`  ${inKb} KB → ${outKb} KB (−${pct}%)`);
  webpFiles.push(webp);
}

const catalog = `/** @generated by tools/convert-menu-noise-to-webp.mjs — do not edit by hand */
const MENU_NOISE_DIR = '/assets/menu noise';

export const MENU_NOISE_WEBP_FILES: readonly string[] = [
${webpFiles.map((f) => `  ${JSON.stringify(f)},`).join('\n')}
];

export function menuNoiseWebpUrl(fileName: string): string {
  return \`\${MENU_NOISE_DIR}/\${encodeURI(fileName)}\`;
}

export const MENU_NOISE_WEBP_URLS: readonly string[] = MENU_NOISE_WEBP_FILES.map(
  menuNoiseWebpUrl,
);

/** Desktop / mouse-first viewport — hide menu noise on phones and tablets. */
export function isDesktopMenuNoiseViewport(): boolean {
  return window.matchMedia('(hover: hover) and (pointer: fine)').matches;
}

export function pickRandomMenuNoiseWebpUrl(): string {
  const urls = MENU_NOISE_WEBP_URLS;
  return urls[Math.floor(Math.random() * urls.length)] ?? urls[0]!;
}
`;

fs.writeFileSync(catalogPath, catalog, 'utf8');
console.log(`\nWrote ${path.relative(projectRoot, catalogPath)} (${webpFiles.length} assets).`);
